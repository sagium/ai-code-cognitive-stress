function Read-WidgetPos {
    try {
        if (Test-Path $posFile) {
            $j = Get-Content $posFile -Raw | ConvertFrom-Json
            return [System.Drawing.Point]::new([int]$j.X, [int]$j.Y)
        }
    } catch { }
    return $null
}

function Save-WidgetPos([System.Windows.Forms.Form] $form) {
    try {
        $dir = Split-Path $posFile
        if (-not (Test-Path $dir)) { New-Item -ItemType Directory -Path $dir -Force | Out-Null }
        @{ X = $form.Left; Y = $form.Top } | ConvertTo-Json | Set-Content $posFile -Encoding UTF8
    } catch { }
}

function Clamp-ToScreen([System.Drawing.Point] $pt, [int] $w, [int] $h) {
    # Keep at least 50px visible on the primary screen.
    $screens = [System.Windows.Forms.Screen]::AllScreens
    foreach ($scr in $screens) {
        $b = $scr.WorkingArea
        if ($pt.X -ge ($b.Right - 50) -or $pt.Y -ge ($b.Bottom - 50) `
            -or ($pt.X + $w) -le ($b.Left + 50) -or ($pt.Y + $h) -le ($b.Top + 50)) {
            continue
        }
        # On this screen: clamp so the title-bar area is reachable.
        $x = [Math]::Max($b.Left - $w + 50, [Math]::Min($pt.X, $b.Right - 50))
        $y = [Math]::Max($b.Top,             [Math]::Min($pt.Y, $b.Bottom - 50))
        return [System.Drawing.Point]::new($x, $y)
    }
    # Not on any screen — reset to primary top-right corner.
    $pa = [System.Windows.Forms.Screen]::PrimaryScreen.WorkingArea
    return [System.Drawing.Point]::new($pa.Right - $w - 36, 36)
}

# ---------------------------------------------------------------------------
# 6. Form region helper — clips the window to a rounded rectangle so the
#    corners outside the card's border-radius are transparent rather than
#    showing the form's background colour.  The card's outer border-radius is
#    24 px; we scale by $script:dpiScale so the clip stays sharp on HiDPI.
#    Called once on startup and re-applied on every SizeChanged event.
# ---------------------------------------------------------------------------

function Set-FormRegion([System.Windows.Forms.Form] $f) {
    $radius = [int](24 * $script:dpiScale)
    $w = $f.Width
    $h = $f.Height
    if ($w -lt $radius * 2 -or $h -lt $radius * 2) { return }
    $path = [System.Drawing.Drawing2D.GraphicsPath]::new()
    $path.AddArc(0,              0,              $radius * 2, $radius * 2, 180, 90)
    $path.AddArc($w - $radius*2, 0,              $radius * 2, $radius * 2, 270, 90)
    $path.AddArc($w - $radius*2, $h - $radius*2, $radius * 2, $radius * 2,   0, 90)
    $path.AddArc(0,              $h - $radius*2, $radius * 2, $radius * 2,  90, 90)
    $path.CloseAllFigures()
    $old = $f.Region
    $f.Region = [System.Drawing.Region]::new($path)
    if ($null -ne $old) { $old.Dispose() }
    $path.Dispose()
}

