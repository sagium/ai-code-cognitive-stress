# ---------------------------------------------------------------------------
# 13. Height bridge: document.title = "cogstress:h:<px>" → resize the form.
#     Also fire getBoundingClientRect on NavigationCompleted as the initial
#     fallback measure (for a static non-tabbed card, which sets no title).
# ---------------------------------------------------------------------------

$wv.CoreWebView2.add_DocumentTitleChanged({
    param($s, $e)
    $t = $wv.CoreWebView2.DocumentTitle
    # Grade bridge: 'cogstress:rate:<day>:<grade>[:<nonce>]' — invoke the CLI
    # with --rate so the sidecar is written. The nonce (if present) is stripped.
    # The host re-runs --emit-html-card on its refresh timer; the next refresh
    # reads the sidecar and the grader collapses to the confirmation state.
    if ($t -match '^cogstress:rate:(\d{4}-\d{2}-\d{2}):([0-2])(?::\d+)?$') {
        $rateDay   = $Matches[1]
        $rateGrade = $Matches[2]
        try {
            $psiRate = [System.Diagnostics.ProcessStartInfo]::new()
            $psiRate.FileName               = $Command
            $psiRate.Arguments              = "--rate $rateDay`:$rateGrade"
            $psiRate.UseShellExecute        = $false
            $psiRate.RedirectStandardOutput = $true
            $psiRate.RedirectStandardError  = $true
            $psiRate.CreateNoWindow         = $true
            $psiRate.StandardOutputEncoding = [System.Text.Encoding]::UTF8
            $psiRate.StandardErrorEncoding  = [System.Text.Encoding]::UTF8
            $psiRate.EnvironmentVariables["PYTHONUTF8"] = "1"
            $procRate = [System.Diagnostics.Process]::Start($psiRate)
            $procRate.WaitForExit() | Out-Null
        } catch { }
        return
    }
    # Compact toggle: 'cogstress:compact:<0|1>[:<nonce>]' — persist via
    # `aicogstress --set-compact <true|false>`, then re-render immediately so
    # the card resizes without waiting for the refresh timer.
    if ($t -match '^cogstress:compact:([01])(?::\d+)?$') {
        $compactVal = if ($Matches[1] -eq '1') { 'true' } else { 'false' }
        try {
            $psiC = [System.Diagnostics.ProcessStartInfo]::new()
            $psiC.FileName               = $Command
            $psiC.Arguments              = "--set-compact $compactVal"
            $psiC.UseShellExecute        = $false
            $psiC.RedirectStandardOutput = $true
            $psiC.RedirectStandardError  = $true
            $psiC.CreateNoWindow         = $true
            $psiC.StandardOutputEncoding = [System.Text.Encoding]::UTF8
            $psiC.StandardErrorEncoding  = [System.Text.Encoding]::UTF8
            $psiC.EnvironmentVariables["PYTHONUTF8"] = "1"
            $procC = [System.Diagnostics.Process]::Start($psiC)
            $procC.WaitForExit() | Out-Null
        } catch { }
        $html = Get-CardHtml
        $wv.CoreWebView2.NavigateToString($html)
        return
    }
    # View tab persist: 'cogstress:view:<key>[:<nonce>]' — persist only via
    # `aicogstress --set-view <key>`. No NavigateToString re-render: the card's
    # own script already switched the visible tab client-side; re-rendering here
    # would flicker/snap back to the current view. The next timer refresh will
    # open to the persisted tab.
    if ($t -match '^cogstress:view:(today|week|month|year)(?::\d+)?$') {
        $viewKey = $Matches[1]
        try {
            $psiV = [System.Diagnostics.ProcessStartInfo]::new()
            $psiV.FileName               = $Command
            $psiV.Arguments              = "--set-view $viewKey"
            $psiV.UseShellExecute        = $false
            $psiV.RedirectStandardOutput = $true
            $psiV.RedirectStandardError  = $true
            $psiV.CreateNoWindow         = $true
            $psiV.StandardOutputEncoding = [System.Text.Encoding]::UTF8
            $psiV.StandardErrorEncoding  = [System.Text.Encoding]::UTF8
            $psiV.EnvironmentVariables["PYTHONUTF8"] = "1"
            $procV = [System.Diagnostics.Process]::Start($psiV)
            $procV.WaitForExit() | Out-Null
        } catch { }
        return
    }
    # Height bridge: 'cogstress:h:<px>'
    if ($t -match '^cogstress:h:(\d+)$') {
        $h = [int]$Matches[1]
        if ($h -gt 0) {
            $form.ClientSize = [System.Drawing.Size]::new($script:scaledCardWidth, [int]($h * $script:dpiScale) + $script:dragBarHeight)
            Save-WidgetPos $form
        }
    }
})

# Static-card fallback height. The tabbed card resizes via the title bridge
# above; a static card sets no title, so measure it with getBoundingClientRect
# after navigation. We can't read the ExecuteScriptAsync result in a
# .ContinueWith continuation — that runs on a threadpool thread with no
# PowerShell runspace and would silently never execute. Instead, kick the
# script off on NavigationCompleted and poll its task from a short
# WinForms.Timer, whose Tick fires on the UI thread (via the message loop, so
# it has a runspace — the same mechanism as the refresh timer below).
$script:measureTask = $null
$measurePoll = [System.Windows.Forms.Timer]::new()
$measurePoll.Interval = 150
$measurePoll.add_Tick({
    if ($null -eq $script:measureTask -or -not $script:measureTask.IsCompleted) { return }
    $measurePoll.Stop()
    try {
        $h = 0
        if ([int]::TryParse(($script:measureTask.Result -replace '"', ''), [ref] $h) -and $h -gt 0) {
            $form.ClientSize = [System.Drawing.Size]::new($script:scaledCardWidth, [int]($h * $script:dpiScale) + $script:dragBarHeight)
            Save-WidgetPos $form
        }
    } catch { }
    $script:measureTask = $null
})

$wv.CoreWebView2.add_NavigationCompleted({
    param($s, $e)
    $script:measureTask = $wv.CoreWebView2.ExecuteScriptAsync(
        "Math.ceil(document.querySelector('.cogstress')?" +
        "document.querySelector('.cogstress').getBoundingClientRect().height:0)")
    $measurePoll.Start()
})

