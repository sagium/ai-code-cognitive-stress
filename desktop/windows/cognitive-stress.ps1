#Requires -Version 5.1
<#
.SYNOPSIS
  Cognitive Stress — Windows desktop widget. Thin WebView2 host only.

.DESCRIPTION
  The card itself — TODAY's full daily view (composite, sparkline, off-hours
  nag, per-hour concurrency chart, axis tiles) — is rendered by the CLI:
  `aicogstress --emit-html-card` prints one self-contained HTML fragment
  (inline CSS + SVG, no scripts), built by ai_code_cognitive_stress/output/widget_card.py.
  That module is the SINGLE renderer shared with the KDE Plasma widget and
  the macOS Übersicht widget, so the surfaces can't drift. This file just
  runs the CLI on a timer and shows its output in a WebView2 control.

  Private: shells out to a local CLI and renders its stdout from memory
  (NavigateToString, no URL loaded). The card contains no scripts and no
  external references; the only JavaScript run in the view is our own
  one-liner that measures the card's height.

  No network calls at runtime. The WebView2 DLLs in lib/ are populated by
  fetch-webview2.ps1 (a one-time maintainer/install-time action). The host
  only navigates via NavigateToString (NavigationStarting cancels anything
  else). The card is fully self-contained (inline CSS + SVG), so this is
  purely defensive.

.PARAMETER RefreshSeconds
  How often to re-run aicogstress (default: 60, minimum: 10).
  Override: set the AICOGSTRESS_REFRESH_SECONDS environment variable.

.PARAMETER Command
  Full path or name of the aicogstress CLI (default: "aicogstress").
  A GUI process may not inherit your shell PATH — if the card stays blank,
  pass the absolute path here or set AICOGSTRESS_CMD.
  Find it with: (Get-Command aicogstress).Source

.PARAMETER SelfTest
  Headless CI mode: load -CardFile <path>, wait for a height measurement,
  print "SELFTEST OK h=<px> w=<px>", and exit 0. Exits 1 on timeout/failure.
  Does not show the tray icon or enter the normal run loop.

.PARAMETER CardFile
  Path to a pre-rendered card HTML file (used with -SelfTest; the demo card
  generated by: python scripts/generate_demo_report.py --dayview-card card.html).

.EXAMPLE
  # Normal launch (add to your Startup folder via install.py --windows):
  powershell -NoProfile -ExecutionPolicy Bypass -WindowStyle Hidden `
    -File "%LOCALAPPDATA%\Programs\aicogstress\widget\cognitive-stress.ps1"

.EXAMPLE
  # CI self-test:
  powershell -ExecutionPolicy Bypass -File cognitive-stress.ps1 `
    -SelfTest -CardFile card.html

.NOTES
  Install: python install.py --windows (from the repo root).
  Source:  desktop/windows/cognitive-stress.ps1
  DLLs:   desktop/windows/fetch-webview2.ps1 (populates lib/).
#>

[CmdletBinding()]
param(
    [int]    $RefreshSeconds = 0,         # 0 = read env / use default 60
    [string] $Command        = "",         # "" = read env / use "aicogstress"
    [switch] $SelfTest,
    [string] $CardFile       = ""
)

Set-StrictMode -Version Latest
$ErrorActionPreference = "Stop"

# ---------------------------------------------------------------------------
# 0. Resolve parameters from env overrides
# ---------------------------------------------------------------------------

if ($RefreshSeconds -le 0) {
    $envSec = [int]($env:AICOGSTRESS_REFRESH_SECONDS)
    $RefreshSeconds = if ($envSec -gt 0) { $envSec } else { 60 }
}
$RefreshSeconds = [Math]::Max(10, $RefreshSeconds)

if ($Command -eq "") {
    $Command = if ($env:AICOGSTRESS_CMD) { $env:AICOGSTRESS_CMD } else { "aicogstress" }
}

# ---------------------------------------------------------------------------
# 1. Load vendored WebView2 + WinForms DLLs
#    All three must exist in lib/ (populated by fetch-webview2.ps1).
# ---------------------------------------------------------------------------

# Join-Path takes only two components on Windows PowerShell 5.1 (the 3+ arg
# form is PowerShell 6+), so nest the calls.
$libDir = Join-Path (Join-Path $PSScriptRoot "lib") "net45"

# The managed Core.dll P/Invokes into the native WebView2Loader.dll, which is
# resolved by the standard DLL search order — and that does NOT include this
# script's directory (the process dir is wherever powershell.exe lives,
# i.e. System32). fetch-webview2.ps1 vendors the loader under
# lib/runtimes/<rid>/native; prepend that dir to PATH so the loader is found.
# (If it's absent, the Evergreen Runtime supplies one — this is belt-and-braces.)
$rid = if ([Environment]::Is64BitProcess) {
    if ([System.Runtime.InteropServices.RuntimeInformation]::ProcessArchitecture -eq 'Arm64') { "win-arm64" } else { "win-x64" }
} else { "win-x86" }
$nativeDir = Join-Path (Join-Path (Join-Path (Join-Path $PSScriptRoot "lib") "runtimes") $rid) "native"
if (Test-Path (Join-Path $nativeDir "WebView2Loader.dll")) {
    $env:PATH = "$nativeDir;$env:PATH"
}

$requiredDlls = @(
    "Microsoft.Web.WebView2.Core.dll",
    "Microsoft.Web.WebView2.WinForms.dll"
)

foreach ($dll in $requiredDlls) {
    $dllPath = Join-Path $libDir $dll
    if (-not (Test-Path $dllPath)) {
        $msg = @"
ERROR: Missing WebView2 managed assembly: $dllPath

Run the fetch script to download the vendored DLLs:
  powershell -ExecutionPolicy Bypass -File "$PSScriptRoot\fetch-webview2.ps1"

Or install via install.py (which calls the fetch script automatically):
  python install.py --windows
"@
        [System.Console]::Error.WriteLine($msg)
        exit 1
    }
    Add-Type -Path $dllPath
}

# WinForms is part of the .NET Framework on Windows — no separate DLL needed.
Add-Type -AssemblyName System.Windows.Forms
Add-Type -AssemblyName System.Drawing

# ---------------------------------------------------------------------------
# 2. DPI awareness — Per-Monitor-V2 so the frameless window stays sharp on
#    HiDPI / multi-monitor setups. Best-effort: we call the modern API first
#    (Windows 10 1607+) and fall back to the older SetProcessDPIAware if
#    unavailable. Degrade silently — the widget still works, just not DPI-scaled.
# ---------------------------------------------------------------------------

$dpiCode = @'
using System;
using System.Runtime.InteropServices;

public static class DpiHelper {
    [DllImport("user32.dll")]
    private static extern bool SetProcessDPIAware();

    [DllImport("user32.dll", SetLastError = true)]
    private static extern bool SetProcessDpiAwarenessContext(IntPtr value);

    // DPI_AWARENESS_CONTEXT_PER_MONITOR_AWARE_V2 = -4
    public static void SetAware() {
        try {
            // Windows 10 1607+ Per-Monitor-V2
            SetProcessDpiAwarenessContext(new IntPtr(-4));
        } catch {
            try {
                SetProcessDPIAware();
            } catch { /* not critical */ }
        }
    }
}
'@
try {
    Add-Type -TypeDefinition $dpiCode -Language CSharp
    [DpiHelper]::SetAware()
} catch {
    # DPI awareness is best-effort; don't abort the widget over it.
}

