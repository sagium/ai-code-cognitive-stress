function Invoke-AicogstressCard {
    <#
    .SYNOPSIS Run aicogstress --emit-html-card, return [pscustomobject] with
    stdout/stderr/ExitCode. Never throws.
    #>
    try {
        $psi = [System.Diagnostics.ProcessStartInfo]::new()
        $psi.FileName               = $Command
        $psi.Arguments              = "--emit-html-card --source auto"
        $psi.UseShellExecute             = $false
        $psi.RedirectStandardOutput      = $true
        $psi.RedirectStandardError       = $true
        $psi.CreateNoWindow              = $true
        $psi.StandardOutputEncoding      = [System.Text.Encoding]::UTF8
        $psi.StandardErrorEncoding       = [System.Text.Encoding]::UTF8
        $psi.EnvironmentVariables["PYTHONUTF8"] = "1"

        $proc = [System.Diagnostics.Process]::Start($psi)
        $stdout = $proc.StandardOutput.ReadToEnd()
        $stderr = $proc.StandardError.ReadToEnd()
        $proc.WaitForExit()
        return [pscustomobject]@{
            Stdout   = $stdout.Trim()
            Stderr   = $stderr.Trim()
            ExitCode = $proc.ExitCode
        }
    } catch {
        return [pscustomobject]@{
            Stdout   = ""
            Stderr   = "Failed to start '$Command': $_"
            ExitCode = -1
        }
    }
}

# ---------------------------------------------------------------------------
# 7. Build the card HTML to inject, or the error chip HTML on failure.
# ---------------------------------------------------------------------------

function Get-CardHtml {
    $r = Invoke-AicogstressCard
    if ($r.ExitCode -eq 0 -and $r.Stdout -match 'class="cogstress"') {
        return $HTML_WRAPPER_HEAD + $r.Stdout + $HTML_WRAPPER_TAIL
    }
    $msg = if ($r.Stderr) { $r.Stderr } elseif ($r.ExitCode -ne 0) {
        "'$Command' failed (exit $($r.ExitCode)). Is aicogstress on PATH? Set -Command or AICOGSTRESS_CMD."
    } else {
        "Unexpected output from '$Command' — run it in a terminal to check."
    }
    # Escape HTML entities in the error message.
    $safe = $msg -replace '&','&amp;' -replace '<','&lt;' -replace '>','&gt;' -replace '"','&quot;'
    return $ERROR_HTML.Replace('{0}', $safe)
}

# ---------------------------------------------------------------------------
# 8.  WebView2 async initialisation helper — pump the WinForms message loop
#     until CoreWebView2InitializationCompleted fires (or we give up).
# ---------------------------------------------------------------------------

function Wait-WebView2Init([Microsoft.Web.WebView2.WinForms.WebView2] $wv, [int] $TimeoutSec = $script:WV2_INIT_TIMEOUT_SEC) {
    # The completed event fires on the UI thread during DoEvents(). The handler
    # and this loop MUST share state: use script scope on both sides (a
    # function-local $done here would never see the handler's write). Distinct
    # names so we never clobber the built-in automatic $error.
    $script:wv2InitDone = $false
    $script:wv2InitErr  = $null
    $handler = {
        param($s, $e)
        if (-not $e.IsSuccess) { $script:wv2InitErr = $e.InitializationException }
        $script:wv2InitDone = $true
    }
    $wv.add_CoreWebView2InitializationCompleted([System.EventHandler[Microsoft.Web.WebView2.Core.CoreWebView2InitializationCompletedEventArgs]] $handler)

    $deadline = [DateTime]::UtcNow.AddSeconds($TimeoutSec)
    while (-not $script:wv2InitDone -and [DateTime]::UtcNow -lt $deadline) {
        [System.Windows.Forms.Application]::DoEvents()
        Start-Sleep -Milliseconds 50
    }
    $wv.remove_CoreWebView2InitializationCompleted([System.EventHandler[Microsoft.Web.WebView2.Core.CoreWebView2InitializationCompletedEventArgs]] $handler)

    if ($script:wv2InitErr) { throw "WebView2 init failed: $($script:wv2InitErr)" }
    if (-not $script:wv2InitDone) { throw "WebView2 init timed out after $TimeoutSec s" }
}

# ---------------------------------------------------------------------------
# 9.  SELF-TEST mode (CI / headless)
#     Load a pre-rendered card file, wait for the height bridge, report, exit.
# ---------------------------------------------------------------------------

if ($SelfTest) {
    if (-not $CardFile -or -not (Test-Path $CardFile)) {
        [System.Console]::Error.WriteLine("SELFTEST: -CardFile not found: '$CardFile'")
        exit 1
    }

    $cardHtmlRaw = Get-Content $CardFile -Raw -Encoding UTF8
    if ($cardHtmlRaw -notmatch 'class="cogstress"') {
        [System.Console]::Error.WriteLine("SELFTEST: CardFile does not contain class=`"cogstress`"")
        exit 1
    }
    $cardHtml = $HTML_WRAPPER_HEAD + $cardHtmlRaw + $HTML_WRAPPER_TAIL

    $form = [System.Windows.Forms.Form]::new()
    $form.Width           = $CARD_WIDTH
    $form.Height          = $CARD_HEIGHT_FALLBACK
    $form.FormBorderStyle = [System.Windows.Forms.FormBorderStyle]::None
    $form.ShowInTaskbar   = $false
    $form.BackColor       = $cardBg
    # Off-screen (NOT minimized): a minimized host can make WebView2 suspend
    # navigation/rendering, so the height bridge may never fire. An off-screen
    # normal window stays "visible" to WebView2 — JS runs, title bridge fires.
    $form.StartPosition   = [System.Windows.Forms.FormStartPosition]::Manual
    $form.Location        = [System.Drawing.Point]::new(-32000, -32000)

    $form.Show()

    # WebView2 first-init is the flaky step on cold CI runners. Retry once on a
    # fresh control + user-data dir — the second attempt benefits from the
    # now-warm Evergreen runtime — each attempt bounded by $WV2_INIT_TIMEOUT_SEC.
    $wv = $null
    $tmpUdd = $null
    $initErr = $null
    foreach ($attempt in 1..2) {
        $tmpUdd = Join-Path ([System.IO.Path]::GetTempPath()) "aicogstress-selftest-$([System.Guid]::NewGuid().ToString('N'))"
        New-Item -ItemType Directory -Path $tmpUdd -Force | Out-Null
        $wv = [Microsoft.Web.WebView2.WinForms.WebView2]::new()
        $wv.Dock = [System.Windows.Forms.DockStyle]::Fill
        $form.Controls.Add($wv)
        try {
            $wv2Env = [Microsoft.Web.WebView2.Core.CoreWebView2Environment]::CreateAsync($null, $tmpUdd, $null).GetAwaiter().GetResult()
            $wv.EnsureCoreWebView2Async($wv2Env) | Out-Null
            Wait-WebView2Init $wv
            $initErr = $null
            break
        } catch {
            $initErr = $_
            [System.Console]::Error.WriteLine("SELFTEST: WebView2 init attempt $attempt/2 failed: $_")
            try { $form.Controls.Remove($wv); $wv.Dispose() } catch { }
            try { Remove-Item $tmpUdd -Recurse -Force -ErrorAction SilentlyContinue } catch { }
        }
    }
    if ($initErr) {
        [System.Console]::Error.WriteLine("SELFTEST FAIL: WebView2 init error: $initErr")
        $form.Close()
        exit 1
    }

    # Disable remote features (defensive; same as normal mode).
    $wv.CoreWebView2.Settings.AreDevToolsEnabled             = $false
    $wv.CoreWebView2.Settings.AreDefaultContextMenusEnabled  = $false
    $wv.CoreWebView2.Settings.IsStatusBarEnabled             = $false
    $wv.CoreWebView2.Settings.IsBuiltInErrorPageEnabled      = $false

    # Block any navigation that isn't our injected content.
    $wv.CoreWebView2.add_NavigationStarting({
        param($s, $e)
        $uri = $e.Uri
        if ($uri -notmatch '^(about:|data:)') {
            $e.Cancel = $true
        }
    })

    $measuredH = 0
    $measuredW = 0

    # Height bridge: parse cogstress:h:<px> from document.title.
    $wv.CoreWebView2.add_DocumentTitleChanged({
        param($s, $e)
        $t = $wv.CoreWebView2.DocumentTitle
        if ($t -match '^cogstress:h:(\d+)$') {
            $script:measuredH = [int]$Matches[1]
        }
    })

    # NavigationCompleted just flips a flag — measurement happens on this UI
    # thread below.
    $script:navDone = $false
    $wv.CoreWebView2.add_NavigationCompleted({
        param($s, $e)
        $script:navDone = $true
    })

    $wv.CoreWebView2.NavigateToString($cardHtml)

    # Two height paths: the title bridge (cogstress:h:<px>, the tabbed card's
    # fast path, handled on the UI thread above) and a getBoundingClientRect
    # poll (reliable fallback for a static card). We poll ExecuteScriptAsync's
    # task from THIS UI-thread loop — reading .Result once .IsCompleted — rather
    # than a .ContinueWith continuation: a continuation runs on a threadpool
    # thread with no PowerShell runspace and would never execute.
    $measureTask = $null
    $deadline = [DateTime]::UtcNow.AddSeconds($SELFTEST_DEADLINE_SEC)
    while ([DateTime]::UtcNow -lt $deadline) {
        [System.Windows.Forms.Application]::DoEvents()
        if ($measuredH -gt 0) { break }
        if ($script:navDone -and $null -eq $measureTask) {
            $measureTask = $wv.CoreWebView2.ExecuteScriptAsync(
                "Math.ceil(document.querySelector('.cogstress')?" +
                "document.querySelector('.cogstress').getBoundingClientRect().height:0)")
        }
        if ($null -ne $measureTask -and $measureTask.IsCompleted) {
            try {
                # A number result is returned JSON-encoded (e.g. 520); strip any
                # quotes defensively and parse.
                $h = 0
                if ([int]::TryParse(($measureTask.Result -replace '"', ''), [ref] $h) -and $h -gt 0) {
                    $script:measuredH = $h
                }
            } catch { }
            # Result arrived but layout not ready yet (0) — re-measure next loop.
            if ($measuredH -le 0) { $measureTask = $null }
        }
        Start-Sleep -Milliseconds 100
    }

    $form.Close()
    try { Remove-Item $tmpUdd -Recurse -Force -ErrorAction SilentlyContinue } catch { }

    if ($measuredH -gt 0) {
        $w = if ($measuredW -gt 0) { $measuredW } else { $CARD_WIDTH }
        Write-Host "SELFTEST OK h=$measuredH w=$w"
        exit 0
    } else {
        [System.Console]::Error.WriteLine("SELFTEST FAIL: height not measured within ${SELFTEST_DEADLINE_SEC}s")
        exit 1
    }
}

# ---------------------------------------------------------------------------
# 10. NORMAL MODE — build the WinForms UI
# ---------------------------------------------------------------------------

# DPI scale factor — read once at startup; assumes zoom is fixed per screen.
# WebView2 is always per-monitor DPI aware, so CSS pixels scale with DPI.
# WinForms may not be (depends on PowerShell's process DPI awareness), so we
# scale the form dimensions explicitly to match WebView2's physical footprint.
$script:dpiScale        = 1.0
$script:scaledCardWidth = $CARD_WIDTH
$script:dragBarHeight   = 8
try {
    $g = [System.Drawing.Graphics]::FromHwnd([IntPtr]::Zero)
    $script:dpiScale        = [double]$g.DpiX / 96.0
    $script:scaledCardWidth = [int]($CARD_WIDTH * $script:dpiScale)
    $script:dragBarHeight   = [Math]::Max(6, [int](8 * $script:dpiScale))
    $g.Dispose()
} catch { }

$form = [System.Windows.Forms.Form]::new()
$form.Text            = "aicogstress"
$form.FormBorderStyle = [System.Windows.Forms.FormBorderStyle]::None
$form.TopMost         = $false
$form.ShowInTaskbar   = $false
$form.StartPosition   = [System.Windows.Forms.FormStartPosition]::Manual
$form.BackColor       = $cardBg
$form.Width           = $script:scaledCardWidth
$form.Height          = [int]($CARD_HEIGHT_FALLBACK * $script:dpiScale)

# Re-apply the rounded clip any time the form is resized (initial size,
# height-bridge update, or manual resize).
$form.add_SizeChanged({ Set-FormRegion $form })
Set-FormRegion $form

# Restore saved position (clamped to visible screen).
$savedPos = Read-WidgetPos
if ($savedPos) {
    $clamped = Clamp-ToScreen $savedPos $script:scaledCardWidth $form.Height
    $form.Location = $clamped
} else {
    # Default: top-right corner of primary screen with a 36px margin.
    $pa = [System.Windows.Forms.Screen]::PrimaryScreen.WorkingArea
    $form.Location = [System.Drawing.Point]::new($pa.Right - $script:scaledCardWidth - 36, 36)
}

