$WV2_INIT_TIMEOUT_SEC = 90
if ($env:AICOGSTRESS_WV2_INIT_TIMEOUT_SEC) {
    $parsed = 0
    if ([int]::TryParse($env:AICOGSTRESS_WV2_INIT_TIMEOUT_SEC, [ref] $parsed) -and $parsed -gt 0) {
        $WV2_INIT_TIMEOUT_SEC = $parsed
    }
}

